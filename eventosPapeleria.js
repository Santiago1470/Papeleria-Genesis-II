var clic = 1;

function abrirDetalles() {
  if (clic == 1) {
    cer = document.getElementById('cerrar');
    abr = document.getElementById('abrir');
    det = document.getElementById('texto1');
    lin = document.getElementById('linea');
    abr.style.display = 'none';
    cer.style.display = 'block';
    det.style.display = 'block';
    lin.style.display = 'block';
    clic = clic + 1;

  } else {
    cer = document.getElementById('cerrar');
    abr = document.getElementById('abrir');
    det = document.getElementById('texto1');
    lin = document.getElementById('linea');
    abr.style.display = 'block';
    cer.style.display = 'none';
    det.style.display = 'none';
    lin.style.display = 'none';
    clic = 1;
  }
}

